package com.vanghee.responseModel;

public class GetResResult {

	
	private String code ;
	private String benCode ;
	private String accNo ;
	private String ifsc ;
	private String name ;
	private String email ;
	private String mobile ;
	private String responseStatus ;
	private String successOrErrorMsg ;
	public String getCode() {
		return code;
	}
	public void setCode(String code) {
		this.code = code;
	}
	public String getBenCode() {
		return benCode;
	}
	public void setBenCode(String benCode) {
		this.benCode = benCode;
	}
	public String getAccNo() {
		return accNo;
	}
	public void setAccNo(String accNo) {
		this.accNo = accNo;
	}
	public String getIfsc() {
		return ifsc;
	}
	public void setIfsc(String ifsc) {
		this.ifsc = ifsc;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getEmail() {
		return email;
	}
	public void setEmail(String email) {
		this.email = email;
	}
	public String getMobile() {
		return mobile;
	}
	public void setMobile(String mobile) {
		this.mobile = mobile;
	}
	public String getResponseStatus() {
		return responseStatus;
	}
	public void setResponseStatus(String responseStatus) {
		this.responseStatus = responseStatus;
	}
	public String getSuccessOrErrorMsg() {
		return successOrErrorMsg;
	}
	public void setSuccessOrErrorMsg(String successOrErrorMsg) {
		this.successOrErrorMsg = successOrErrorMsg;
	}

	 
}
